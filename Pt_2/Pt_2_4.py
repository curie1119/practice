from math import sqrt
a = int(input("Введите значение коэффициента a: "))
b = int(input("Введите значение коэффициента b: "))
c = int(input("Введите значение коэффициента c: "))
D = b ** 2 - 4 * a * c
if D > 0:
    x1 = (-b + sqrt(D)) / (2 * a)
    x2 = (-b - sqrt(D)) / (2 * a)
    print("Корни квадратного уравнения:", x1, ",", x2)
elif D == 0:
    x = -b / (2 * a)
    print("Корень квадратного уравнения:", x)
else:
    print("Корней у квадратного уравнения нет")
